#include<unistd.h>

void	ft_putnbr(int nb)
{
	int	n;

	if (nb == -2147483648)
	{
		write (1, "-2", 2);
		nb = 147483648;
	}
	if (nb < 0)
	{
		write (1, "-", 1);
		nb = -nb;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	n = nb % 10 + '0';
	write (1, &n, 1);
}
